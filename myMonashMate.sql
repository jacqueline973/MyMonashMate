CREATE TABLE UNIT(
    UNIT_CODE VARCHAR(7) NOT NULL,  
    UNIT_NAME VARCHAR(300) NOT NULL,
    PRIMARY KEY (UNIT_CODE)
);
CREATE TABLE COURSE(
    COURSE_CODE VARCHAR(4) NOT NULL,  
    COURSE_NAME VARCHAR(300) NOT NULL,
    PRIMARY KEY (COURSE_CODE)
);
CREATE TABLE COURSE_UNIT(
    COURSE_CODE VARCHAR(4) NOT NULL,  
    UNIT_CODE VARCHAR(7) NOT NULL,
	PRIMARY KEY (COURSE_CODE,UNIT_CODE)
);
CREATE TABLE STUDENT(
    STUDENT_ID INTEGER NOT NULL,  
	NAME VARCHAR(15),
	NICKNAME VARCHAR(15),
	LATITUDE VARCHAR(25),
	LONGITUDE VARCHAR(25),
	NATIVE_LANGUAGE VARCHAR(15),
	FAVOURITE_FOOD VARCHAR(25),
	SUBURB VARCHAR(15),
	PRIMARY KEY (STUDENT_ID)
);
CREATE TABLE STUDENT_SETTING(
    STUDENT_ID INTEGER NOT NULL,  
    ATTRIBUTE VARCHAR(25) NOT NULL,
	RESTRICTED BOOLEAN,
	CHANGED BOOLEAN,
	PRIMARY KEY (STUDENT_ID,ATTRIBUTE)
);
CREATE TABLE STUDENT_UNIT(
    STUDENT_ID INTEGER NOT NULL,  
    UNIT_CODE VARCHAR(7) NOT NULL,
	PRIMARY KEY (STUDENT_ID,UNIT_CODE)
);
CREATE TABLE STUDENT_COURSE(
    STUDENT_ID INTEGER NOT NULL,  
    COURSE_CODE VARCHAR(4) NOT NULL,
	PRIMARY KEY (STUDENT_ID,COURSE_CODE)
);
CREATE TABLE USER_ACCESS(
	id INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
	name VARCHAR(24) NOT NULL,
	password VARCHAR(1024) NOT NULL,
	STUDENT_ID INTEGER,
	CONSTRAINT primary_key PRIMARY KEY (id)
);
